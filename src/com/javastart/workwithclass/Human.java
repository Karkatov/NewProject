package com.javastart.workwithclass;

public class Human {

    private String name;
    private int age;

    // get and set
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }
    public void setAge(int age) {
        this.age = age;
    }
}
